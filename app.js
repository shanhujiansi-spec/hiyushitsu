function renderRecords() {
  const list = document.getElementById("records-list");
  let entries = [...state.savedEntries];

  if (state.recordFilter === "top50") {
    entries = entries.sort((a, b) => b.totalScore - a.totalScore).slice(0, 50);
  }

  if (state.recordMode === "newest") {
    entries.sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
  } else {
    entries.sort((a, b) => b.totalScore - a.totalScore);
  }

  if (entries.length === 0) {
    list.innerHTML = "<p class='muted'>まだ保存された比喩はありません。</p>";
    return;
  }

  list.innerHTML = entries.map(entry => `
    <div class="record-item">
      <p><strong>${entry.totalScore}点</strong></p>
      <p>テーマ：${escapeHtml(entry.themeText)}</p>
      <p>シーン：${escapeHtml(entry.sceneText)}</p>
      <p>${escapeHtml(entry.answer.slice(0, 60))}${entry.answer.length > 60 ? "…" : ""}</p>
      <p class="muted">${new Date(entry.createdAt).toLocaleString("ja-JP")}</p>
      <details>
        <summary>詳細を見る</summary>
        <p>${escapeHtml(entry.answer)}</p>
        <p>明確さ：${entry.scores.clarity} / 20</p>
        <p>構造一致：${entry.scores.structure} / 20</p>
        <p>具体性：${entry.scores.concreteness} / 20</p>
        <p>新鮮さ・必然性：${entry.scores.freshness} / 20</p>
        <p>余韻：${entry.scores.resonance} / 20</p>
        <p>${entry.feedback.map(escapeHtml).join("<br>")}</p>
        <p>ヒント語：${entry.hintWords.map(escapeHtml).join(" / ")}</p>
        <button onclick="deleteEntry('${entry.id}')">削除</button>
      </details>
    </div>
  `).join("");
}

function escapeHtml(str) {
  return String(str)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function evaluateMetaphor(answer, theme, scene) {
  const text = preprocess(answer);
  const clarity = scoreClarity(text);
  const structure = scoreStructure(text, theme, scene);
  const concreteness = scoreConcreteness(text);
  const freshness = scoreFreshness(text, theme, scene);
  const resonance = scoreResonance(text);
  const totalScore = clarity.score + structure.score + concreteness.score + freshness.score + resonance.score;
  const feedback = buildFeedback({
    totalScore, clarity, structure, concreteness, freshness, resonance
  });

  return {
    themeId: theme.id,
    sceneId: scene.id,
    themeText: theme.text,
    sceneText: scene.text,
    answer: text,
    totalScore,
    coreScores: {
      structure: structure.score,
      concreteness: concreteness.score
    },
    scores: {
      clarity: clarity.score,
      structure: structure.score,
      concreteness: concreteness.score,
      freshness: freshness.score,
      resonance: resonance.score
    },
    feedback,
    hintWords: theme.hintWords
  };
}

function scoreClarity(text) {
  let score = 0;
  const length = text.length;

  if (length >= 12 && length <= 60) score += 4;
  else if ((length >= 8 && length <= 11) || (length >= 61 && length <= 90)) score += 3;
  else if ((length >= 5 && length <= 7) || (length >= 91 && length <= 120)) score += 2;
  else if (length > 0) score += 1;

  if (/(ようだ|みたいだ|に似ている|まるで|のような|感じだ)$/.test(text) || /(ようだ|みたいだ|に似ている|まるで|のような|感じだ)/.test(text)) {
    score += 4;
  } else {
    score += 2;
  }

  score += text.length > 0 ? 4 : 0;

  const conjunctions = countMatches(text, ["そして", "しかし", "だから", "つまり"]);
  score += conjunctions <= 1 ? 4 : 2;

  const abstractCount = countMatches(text, abstractWords);
  score += abstractCount === 0 ? 4 : abstractCount <= 2 ? 2 : 1;

  return { score: Math.min(score, 20) };
}

function scoreStructure(text, theme, scene) {
  const themeWords = flattenRelatedWords(theme.relatedWords);
  const sceneWords = flattenRelatedWords(scene.relatedWords);

  const themeHits = countMatches(text, themeWords);
  const sceneHits = countMatches(text, sceneWords);

  const themeMatch = themeHits >= 4 ? 8 : themeHits >= 3 ? 6 : themeHits >= 2 ? 4 : themeHits >= 1 ? 2 : 0;
  const sceneMatch = sceneHits >= 4 ? 6 : sceneHits >= 3 ? 5 : sceneHits >= 2 ? 3 : sceneHits >= 1 ? 1 : 0;

  const metaphorForm = /(ようだ|みたいだ|に似ている|まるで|のような|感じだ)/.test(text);
  const conjunctions = countMatches(text, ["そして", "しかし", "だから", "つまり"]);
  const integration = metaphorForm && conjunctions <= 1 ? 6 : metaphorForm ? 4 : 2;

  return { score: Math.min(themeMatch + sceneMatch + integration, 20) };
}

function scoreConcreteness(text) {
  const concreteHits = countMatches(text, concreteWords);
  const sensoryHits = countMatches(text, sensoryWords);

  const concrete = concreteHits >= 2 ? 8 : concreteHits === 1 ? 5 : 1;
  const sensory = sensoryHits >= 2 ? 6 : sensoryHits === 1 ? 3 : 1;
  const scene = (concreteHits + sensoryHits) >= 3 ? 6 : (concreteHits + sensoryHits) >= 2 ? 4 : 1;

  return { score: Math.min(concrete + sensory + scene, 20) };
}

function scoreFreshness(text, theme, scene) {
  const clicheHits = countMatches(text, clichePhrases);
  const clicheAvoid = clicheHits === 0 ? 6 : clicheHits === 1 ? 3 : 0;

  const concreteHits = countMatches(text, concreteWords);
  const freshChoice = concreteHits >= 2 ? 6 : concreteHits === 1 ? 4 : 2;

  const themeHits = countMatches(text, flattenRelatedWords(theme.relatedWords));
  const sceneHits = countMatches(text, flattenRelatedWords(scene.relatedWords));
  const naturalFit = (themeHits >= 1 && sceneHits >= 1) ? 7 : themeHits >= 1 ? 4 : 2;

  return { score: Math.min(clicheAvoid + freshChoice + naturalFit, 20) };
}

function scoreResonance(text) {
  const explanationHits = countMatches(text, explanationWords);
  const notOverExplained = explanationHits === 0 ? 6 : explanationHits === 1 ? 4 : 1;

  const lingeringImage = /(ようだ|みたいだ|感じだ|ような)$/.test(text) ? 8 :
                         /(ようだ|みたいだ|感じだ|ような)/.test(text) ? 5 : 2;

  const sensoryHits = countMatches(text, sensoryWords);
  const meaningExpansion = sensoryHits >= 2 ? 6 : sensoryHits === 1 ? 4 : 2;

  return { score: Math.min(notOverExplained + lingeringImage + meaningExpansion, 20) };
}

function buildFeedback(result) {
  const scoreMap = {
    clarity: result.clarity.score,
    structure: result.structure.score,
    concreteness: result.concreteness.score,
    freshness: result.freshness.score,
    resonance: result.resonance.score
  };

  const sorted = Object.entries(scoreMap).sort((a, b) => b[1] - a[1]);
  const highest = sorted[0][0];
  const lowest = sorted[sorted.length - 1][0];

  return [
    buildOverallLine(result.totalScore),
    buildStrengthLine(highest),
    buildImproveLine(lowest)
  ];
}

function buildOverallLine(total) {
  if (total >= 90) return "非常によくできた比喩です。像・構造・余韻のバランスが取れています。";
  if (total >= 80) return "強い比喩です。全体としてよくまとまっており、印象が残ります。";
  if (total >= 70) return "良い比喩です。成立しており、いくつか強みも見えます。";
  if (total >= 60) return "比喩としての形はありますが、まだ伸ばせる部分があります。";
  if (total >= 50) return "比喩として読めますが、やや説明や定番表現に寄っています。";
  return "方向性はありますが、像や構造がまだ弱めです。";
}

